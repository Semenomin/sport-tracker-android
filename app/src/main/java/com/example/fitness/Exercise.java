package com.example.fitness;

public class Exercise {
    private String name;
    private String type;
    private int podhody;
    private int kollvo;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getPodhody() {
        return podhody;
    }

    public void setPodhody(int podhody) {
        this.podhody = podhody;
    }

    public int getKollvo() {
        return kollvo;
    }

    public void setKollvo(int kollvo) {
        this.kollvo = kollvo;
    }
}
